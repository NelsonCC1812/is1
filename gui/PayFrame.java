        private void initThings() {

                txtreserveNum.setText(Integer.toString(reservation.id()));
                txtreserveNum.setEditable(false);

                lsttickets.setModel(new javax.swing.AbstractListModel<String>() {
                        String[] strings = reservation.tickets().stream().map(e -> e.passenger().name()).toList()
                                        .toArray(new String[reservation.tickets().size()]);

                        public int getSize() {
                                return strings.length;
                        }

                        public String getElementAt(int i) {
                                return strings[i];
                        }
                });

                lstaddtitionalServices.setModel(new javax.swing.AbstractListModel<String>() {
                        String[] strings = reservation.additionalServices().stream().map(e -> e.description()).toList()
                                        .toArray(new String[reservation.additionalServices().size()]);

                        public int getSize() {
                                return strings.length;
                        }

                        public String getElementAt(int i) {
                                return strings[i];
                        }
                });

                txtvehicle.setText(reservation.vehicle().toString());
                txtcost.setText(Double.toString(reservation.calcCost()));

                btncancel.addActionListener(e -> {
                        JFrame frame = ctx.get(ctx.size() - 1);
                        ctx.remove(frame);
                        frame.setVisible(true);
                        this.dispose();
                });

                btnpay.addActionListener(e -> {
                        reservation.invoice(new Invoice(reservation.calcCost(), LocalDateTime.now()));
                        model.addReservation(reservation);

                        JFrame frame = ctx.get(0);
                        ctx.remove(frame);
                        for (JFrame fr : ctx)
                                fr.dispose();
                        frame.setVisible(true);
                        this.dispose();
                });

        }

